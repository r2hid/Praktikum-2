totalPutaran = 5
telahBerlari = 14
sekaliPutaran = telahBerlari / totalPutaran
phi = 3.14
jariJariLingkaran = sekaliPutaran / (2 * phi)
print("Diketahui :")
print("Pak Dengklek mengelilingi taman =", totalPutaran, "Putaran")
print("Jarak tempuh Pak Dengklek =", telahBerlari, "Kilometer")
print("Jawaban :")
print("Jari-jari taman yang dikelilingi Pak Dengklek adalah", round(jariJariLingkaran,2), "Kilometer")